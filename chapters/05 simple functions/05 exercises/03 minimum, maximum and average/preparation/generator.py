import os
import random
import subprocess

# set fixed seed for generating test cases
random.seed(123456789)

# locate evaldir
evaldir = os.path.join('..', 'evaluation')
if not os.path.exists(evaldir):
    os.makedirs(evaldir)

# locate solutiondir
solutiondir = os.path.join('..', 'solution')
if not os.path.exists(solutiondir):
    os.makedirs(solutiondir)

# configuration settings
tab_name = 'Statistics'
settings = f'''
tab name: {tab_name}
python input without prompt: true
block count: multi
input block size: 3
output block size: 3
comparison: exact match
<LANGUAGE code="nl">
    <fixed from="average" to="gemiddelde" />
    <fixed from="Statistics" to="Statistieken" />
</LANGUAGE>
'''

# generate test data
cases = [(12, 5, 8)]
while len(cases) < 50:
    cases.append(tuple(random.randint(-100, 100) for _ in range(3)))

# configure test files
infile = open(os.path.join(evaldir, '0.in'), 'w')
outfile = open(os.path.join(evaldir, '0.out'), 'w')

# generate unit tests
for stdin in cases:

    # add input to input file
    stdin = '\n'.join(f'{line}' for line in stdin)
    print(stdin, file=infile)

    # generate output to output file
    script = os.path.join(solutiondir, 'solution.en.py')
    process= subprocess.run(
        ['python', script],
        input=stdin,
        encoding='utf-8',
        capture_output=True, shell=True, check=True
    )
    stdout = process.stdout

    # add stdout to output file
    print(stdout, file=outfile, end='')

# add settings to output file
print('-' * 60 + settings, file=outfile, end='')
